    private void searchEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchEditActionPerformed
        String s = searchEditField.getText();
        AddressBook book = new AddressBook();
        try{
            FileReader f = new FileReader("data.txt");
            BufferedReader reade = new BufferedReader(f);

            String line;
            while ((line = reade.readLine())!=null){
                String [] details = line.split("-");
                String nam = details[0];
                String e = details[1];
                String num = details[2];
                String a = details[3];

                book.create(new Address(nam,e,num,a));
            }
            book.editSearch(s);
            reade.close();
        }catch (IOException e){
            e.printStackTrace();
        }

    }//GEN-LAST:event_searchEditActionPerformed

