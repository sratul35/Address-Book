    private void editEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editEditActionPerformed
        String e1 = nameEditField.getText();
        String e2 = emailEditField.getText();
        String e3 = numberEditField.getText();
        String e4 = addressEditField.getText();
        nameEditField.setText("");
        emailEditField.setText("");
        numberEditField.setText("");
        addressEditField.setText("");
        AddressBook book = new AddressBook();
        String searchName = searchEditField.getText();
        File file = new File("data.txt");
        try(BufferedReader buf = new BufferedReader(new FileReader(file))){
            String l;
            while ((l=buf.readLine())!=null){
                String [] details = l.split("-");
                String nam = details[0];
                String e = details[1];
                String num = details[2];
                String a = details[3];
                book.create(new Address(nam,e,num,a));
            }
            book.edit(e1,e2,e3,e4,searchName);
            searchEditField.setText("");
            showDataArea.setText("");
        }catch (IOException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_editEditActionPerformed

